# Function to calculate the perimeter (circumference) of a cylinder
def cylinder_perimeter(radius):
    pi = 3.14159  # Approximation of pi
    return 2 * pi * radius

# Example radius
radius = 5  # Radius of the base of the cylinder

# Calculate the perimeter (circumference)
perimeter = cylinder_perimeter(radius)

# Print the result
print("Perimeter (Circumference) of the cylinder's base:", perimeter)
